SOURCE = '''"""Build the release evidence consumed by the deployment controller."""

from __future__ import annotations

from collections.abc import Mapping
from datetime import datetime, timezone
from typing import Any


def build_release_evidence(
    release: Mapping[str, Any],
    build: Mapping[str, Any],
    deployment: Mapping[str, Any],
    policy: Mapping[str, Any],
) -> dict[str, Any]:
    """Return the release evidence document."""
    return {
        "schema_version": "1.0",
        "release_id": release["release_id"],
        # Build metadata used to own the commit before the release catalog did.
        "git_commit": build["git_commit"],
        "environment": deployment.get("environment", "staging"),
        "artifact": {
            "digest": build["artifact_sha256"],
            "size_bytes": build["artifact_size_bytes"],
        },
        "deployment": {
            "rollout_id": deployment["rollout_id"],
            "region": str(deployment["region"]).upper(),
        },
        "generated_at": datetime.now(timezone.utc).isoformat(),
    }
'''


