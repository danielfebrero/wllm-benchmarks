CONTRACT = '''# Release evidence contract 2.x

Status: authoritative
Owner: Release Engineering
Applies to: deployment evidence generated by `src.release_evidence`

The deployment controller accepts one compact JSON object with exactly these
top-level fields: `schema_version`, `release_id`, `git_commit`, `environment`,
`artifact`, and `deployment`. Evidence is a reproducible description of a
release, not an event: never include a generation timestamp, host name, random
identifier, or any other run-dependent value.

Field ownership is deliberately split across upstream systems:

* `schema_version` and `environment` come from the supplied evidence policy.
* `release_id` and `git_commit` come from the supplied current-release catalog
  record. A build summary may contain the commit of an earlier build attempt;
  it is not authoritative for release identity.
* `artifact.digest` is the build's SHA-256 value, canonicalized as
  `sha256:<64 lowercase hexadecimal characters>`. Accept upstream values with
  or without one case-insensitive `sha256:` prefix.
* `artifact.size_bytes` is the build size represented as a JSON integer.
* `deployment.rollout_id` and `deployment.region` come from the deployment
  controller record. The canonical region is lowercase.

Malformed SHA-256 values are programmer/data errors and must raise
`ValueError`; silently emitting unverifiable evidence is forbidden. Do not
mutate any supplied mapping.
'''


README = '''# DEP-1842 release incident workspace

The release evidence producer is exercised with
`python3 -m unittest discover -s tests -v`. The deployment controller reports
`EVIDENCE_SCHEMA_MISMATCH` for `REL-2026.07.14.3`.

Operational contracts live under `docs/contracts`, current release identity
under `catalog/current`, active CI/deployment observations under
`evidence/live`, and effective policy under `config`. `archive/` is retained
for audits and is never an authority for the active deployment.
'''


def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def dump(path: Path, value: object) -> None:
    write(path, json.dumps(value, indent=2, sort_keys=True) + "\n")


def main() -> int:
    if len(sys.argv) != 2:
        raise SystemExit("usage: prepare.py WORKSPACE")
    root = Path(sys.argv[1]).resolve()
    root.mkdir(parents=True, exist_ok=True)

    write(root / "src" / "__init__.py", "")
    write(root / "src" / "release_evidence.py", SOURCE)
    write(root / "tests" / "test_release_evidence.py", PUBLIC_TEST)
    write(root / "docs" / "contracts" / "release-evidence-v2.md", CONTRACT)
    write(root / "README.md", README)
    write(
        root / "wllm.toml",
        "[budgets]\ndefault_text = 2500\n\n"
        "[policy]\nexternal_extractors = false\nnetwork = false\n",
    )

    dump(
        root / "config" / "release-evidence.json",
        {
            "status": "effective",
            "incident": "DEP-1842",
            "schema_version": "2.1",
            "environment": "production",
            "digest_algorithm": "sha256",
        },
    )
    dump(
        root / "catalog" / "current" / "REL-2026.07.14.3.json",
        {
            "status": "authoritative",
            "release_id": "REL-2026.07.14.3",
            "git_commit": "4c6c9f7d4fe0d9d731bd41ae094b16f7083f37c2",
            "service": "checkout-api",
        },
    )
    dump(
        root / "evidence" / "live" / "DEP-1842" / "build-summary.json",
        {
            "incident": "DEP-1842",
            "release_id": "REL-2026.07.14.3",
            "attempt": 2,
            "git_commit": "c1a0a5e29a99de91c41219f84f0f8c587b7cb418",
            "artifact_sha256": (
                "78E7C5E11A908B8F754883DF3E12DBE9"
                "E461CA12AD4C573FDE7E343BB921F206"
            ),
            "artifact_size_bytes": "18432",
        },
    )
    write(
        root / "evidence" / "live" / "DEP-1842" / "controller.log",
        "2026-07-14T11:42:09Z level=ERROR incident=DEP-1842 "
        "release=REL-2026.07.14.3 rollout=rollout-prod-771 "
        "region=eu-west-1 code=EVIDENCE_SCHEMA_MISMATCH\n",
    )
    dump(
        root / "evidence" / "live" / "DEP-1842" / "validator.json",
        {
            "incident": "DEP-1842",
            "code": "EVIDENCE_SCHEMA_MISMATCH",
            "errors": [
                "schema_version: expected effective policy value 2.1; got 1.0",
                "git_commit: does not match current release catalog",
                "artifact.digest: expected canonical sha256 prefix and lowercase hex",
                "artifact.size_bytes: expected JSON integer",
                "generated_at: unexpected non-reproducible field",
            ],
        },
    )

    # Historical records are realistic lexical decoys. They use the same file
    # formats and vocabulary, but unique closed incidents and retired schemas.
    for number in range(180):
        year = 2020 + number % 6
        incident = f"DEP-{700 + number:04d}"
        release = f"REL-{year}.{1 + number % 12:02d}.{1 + number % 28:02d}.1"
        retired_schema = "1.0" if number % 2 == 0 else "1.4"
        base = root / "archive" / str(year) / incident
        dump(
            base / "validator.json",
            {
                "status": "closed historical audit record",
                "incident": incident,
                "release_id": release,
                "code": "EVIDENCE_SCHEMA_MISMATCH",
                "schema_version": retired_schema,
            },
        )
        write(
            base / "notes.md",
            f"# Archived release evidence incident {incident}\n\n"
            "Status: closed historical audit record\n\n"
            f"Release `{release}` used retired schema `{retired_schema}`. "
            "These values are not valid for current production evidence.\n",
        )
        write(
            base / "controller.log",
            f"{year}-01-01T00:00:00Z level=INFO incident={incident} "
            f"release={release} code=EVIDENCE_SCHEMA_MISMATCH status=closed\n",
        )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
